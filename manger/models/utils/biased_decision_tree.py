from sklearn.tree import DecisionTreeClassifier, DecisionTreeRegressor


class BiasedDecisionTreeRegressor(DecisionTreeRegressor):
    def __init__(
        self,
        *,
        criterion="squared_error",
        splitter="best",
        max_depth=None,
        min_samples_split=2,
        min_samples_leaf=1,
        min_weight_fraction_leaf=0.0,
        max_features=None,
        random_state=None,
        max_leaf_nodes=None,
        min_impurity_decrease=0.0,
        ccp_alpha=0.0,
    ):
        super().__init__(
            criterion=criterion,
            splitter=splitter,
            max_depth=max_depth,
            min_samples_split=min_samples_split,
            min_samples_leaf=min_samples_leaf,
            min_weight_fraction_leaf=min_weight_fraction_leaf,
            max_features=max_features,
            max_leaf_nodes=max_leaf_nodes,
            random_state=random_state,
            min_impurity_decrease=min_impurity_decrease,
            ccp_alpha=ccp_alpha,
        )
        # this property will be used to set non-used features to zero when passed to the random forest
        self.biased_feature_importance = None
        self.bootstrapped_samples = None
        self.leaf_samples = None
        self.leaf_classes = None
        self.leaf_class_assignments = None


